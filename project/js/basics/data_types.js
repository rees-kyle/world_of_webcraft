// strings; to represent text data
let str = "Hello, world!";
console.log(str);

// integers; to represent numeric data
let num = 42;
console.log(num);

// floats
let float = 99.99;
console.log(float);

// booleans; to represent true or false values
let isTrue = true;
console.log(isTrue);

// arrays; ordered collections of values
let mixedArray = [1, "apple", 3.14, true, null];
console.log(mixedArray);

// objects; collections of key-value pairs
let obj = { name: "Kyle", age: 28 }; // 'name:' and 'age:' are keys, '"Kyle"' and '28' are values
console.log(obj);